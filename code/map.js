function drawMap() {
	x = 0;
	drawTile(x,0);
	for (x = 0; x < MAPXSIZE; x++) { 
		for (y = 0; y < MAPYSIZE; y++) {
			drawTile(x,y);
		}
	}
}

function drawTile(x,y) {
	var tileFrame = map[y][x];
	
	//to do: tile mappings
	if (tileFrame == 0) {
		tileFrame = 4;
	}
	if (tileFrame == 1) {
		tileFrame = 0;
	}
	
	var xLoc = x * GRIDSIZE;
	var yLoc = y * GRIDSIZE;
	
	tile = new createjs.Sprite(mapSpriteSheet);
	tile.gotoAndStop(tileFrame);
	tile.x = xLoc;
	tile.y = yLoc;
	stage.addChild(tile);
}